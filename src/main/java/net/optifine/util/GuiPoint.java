package net.optifine.util;

public class GuiPoint
{
    private int x;
    private int y;

    public GuiPoint(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }
}
